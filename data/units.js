// Helper to get all assignments as flat array
export function getAllAssignments() {
  const assignments = [];
  Object.values(units).forEach(unit => {
    Object.values(unit.assignments || {}).forEach(assignment => {
      assignments.push({
        ...assignment,
        unitNumber: unit.number,
        unitTitle: unit.title,
        unitType: unit.type
      });
    });
  });
  return assignments;
}

// Helper to get assignments by unit
export function getAssignmentsByUnit(unitId) {
  const unit = units[unitId];
  if (!unit) return [];
  return Object.values(unit.assignments || {});
}

// Helper to get current/upcoming assignments based on date
export function getActiveAssignments(date = new Date()) {
  const all = getAllAssignments();
  return all.filter(a => {
    if (!a.handOut || !a.handIn) return false;
    const handOut = new Date(a.handOut);
    const resubDate = a.resubmissionDate ? new Date(a.resubmissionDate) : new Date(a.handIn);
    // Active if between hand out and resubmission deadline
    return date >= handOut && date <= resubDate;
  });
}

// Helper to get upcoming assignments (next 4 weeks)
export function getUpcomingAssignments(date = new Date()) {
  const all = getAllAssignments();
  const fourWeeks = new Date(date);
  fourWeeks.setDate(fourWeeks.getDate() + 28);
  return all.filter(a => {
    if (!a.handIn) return false;
    const handIn = new Date(a.handIn);
    return handIn >= date && handIn <= fourWeeks;
  });
}

// Get assignment dropdown options for Forms
export function getAssignmentDropdownOptions() {
  const all = getAllAssignments();
  return all
    .filter(a => a.unitType === 'coursework')
    .map(a => ({
      value: a.id,
      label: `Unit ${a.unitNumber} - ${a.title}`,
      handIn: a.handIn
    }));
}
